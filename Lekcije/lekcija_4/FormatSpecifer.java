/*
 * 	Kako bi ispisali sljedeæu poruku na konzoli?
 * 	“Out of all the things I’ve lost, I miss my mind the most. “
 * 													~ Mark Twain
 *
 */

public class FormatSpecifer {

	public static void main(String[] args) {
		
		System.out.println("\" Out of all the things I've lost,I miss my mind the moust.\"");
		System.out.println("\t\t\t\t ~ Mark Twain");
		
		System.out.printf("\" Out of all the things I've lost,I miss my mind the moust.\"\n");
		System.out.printf("\t\t\t\t ~ Mark Twain");
		
		
	}

}
