package lekcija_2;

public class Varijable {

	public static void main(String[] args) {

		// deklarisati varijable - imenovati varijable
		int broj;
		int rezultat;
		
		// inicijalizirati varijable - dodijeliti vrijednost varijablama
		broj = 5;
		rezultat = broj * 2;
		
		// ispisati rezultat mnozenja dvije varijable
		System.out.println("Rezultat mnozenja brojeva 5 i 2 je: " + rezultat);
	}

}
